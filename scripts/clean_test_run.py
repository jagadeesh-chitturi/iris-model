"""
run_simulations.py
------------------
Batch runner for the mosquito population dynamics C++ simulation engine.

Iterates over a grid of insertion ratios, equilibrium start dates, intervention
durations, and insertion frequencies; writes a per-run config CSV; and invokes
the compiled C++ executable for each parameter combination.

Environment variables (set in .env or shell):
    PROJECT_ROOT  – root directory of the project repository
    OUTPUT_DIR    – root directory where simulation outputs are written

Usage:
    python run_simulations.py
"""

